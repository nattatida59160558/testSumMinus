const minus = require('./minus');
// 5 - 10 เท่ากับ -5
test('5 - 10 เท่ากับ -5', ()=> {
    expect(minus(5,10)).toBe(-5);
});

// 10 - 5 เท่ากับ 5
test('10 - 5 เท่ากับ 5', ()=> {
    expect(minus(10,5)).toBe(5);
});

// -5 - -5 เท่ากับ 0
test('-5 - -5 เท่ากับ 0', ()=> {
    expect(minus(-5,-5)).toBe(0);
});

// -5 - 5 เท่ากับ -10
test('-5 - 5 เท่ากับ -10', ()=> {
    expect(minus(-5,5)).toBe(-10);
});

// 2 - 3 เท่ากับ -1
test('2 - 3 เท่ากับ -1', ()=> {
    expect(minus(2,3)).toBe(-1);
});